window.addEventListener('scroll', () => {
  const section = document.getElementById('hizmetler');
  const cards = document.querySelectorAll('.strict-card'); // Yeni brutalist kartlar
  
  if(!section) return;

  const scrollItems = document.querySelectorAll('.scroll-item');
  const stickyContents = document.querySelectorAll('.sticky-content');
  
  let activeId = 'content-studio';
  scrollItems.forEach((item) => {
    const itemRect = item.getBoundingClientRect();
    if (itemRect.top < window.innerHeight / 2) {
      activeId = item.getAttribute('data-sticky') || 'content-studio';
    }
  });

  // Sol tarafı değiştirir
  stickyContents.forEach((content) => {
    if (content.id === activeId) {
      content.classList.add('active');
    } else {
      content.classList.remove('active');
    }
  });

  // SAĞ TARAF: Kartları tek tek parlatır
  cards.forEach((card) => {
    const cardTop = card.getBoundingClientRect().top;
    const triggerPoint = window.innerHeight * 0.7; // Ekranın %70'ine gelince parlar

    if(cardTop < triggerPoint) {
      card.classList.add('highlight');
    } else {
      card.classList.remove('highlight');
    }
  });
});

