const marqueeItems = {
  tr: ['Web Geliştirme', 'Mobil Uygulama', 'Yapay Zeka', 'SaaS Yazılım', 'Backend', 'Veri Anali̇tiği', 'Cloud Native', 'Kurumsal Çözümler'],
  en: ['Web Development', 'Mobile Apps', 'Artificial Intelligence', 'SaaS Software', 'Backend', 'Data Analytics', 'Cloud Native', 'Enterprise Solutions']
};

function updateMarquee(lang) {
  const inner = document.querySelector('.marquee-inner');
  if (!inner) return;
  const items = [...marqueeItems[lang], ...marqueeItems[lang]];
  inner.innerHTML = items.map(i => `<span>${i}</span><span class="sep">·</span>`).join('');
}

// Extend setLanguage to also update marquee
const _origSetLang = setLanguage;
window.setLanguage = function(lang) {
  _origSetLang(lang);
  updateMarquee(lang);
};
